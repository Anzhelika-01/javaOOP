package interfacesAndAbstraction.birthdayCelebrations;

public class Citizen implements Birthable, Identifiable {
    private String name, id, birthDate;
    private int age;

    Citizen(String name, int age, String id, String birthDate) {
        this.name = name;
        this.age = age;
        this.id = id;
        this.birthDate = birthDate;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    @Override
    public String getId() {
        return id;
    }

    @Override
    public String getBirthDate() {
        return birthDate;
    }
}